import { Request, Response, NextFunction } from 'express';
import { ZodType, ZodError } from 'zod'; 

export const validate = (schema: ZodType) => (req: Request, _res: Response, next: NextFunction) => {
    try {
        schema.parse({
            body: req.body,
            query: req.query,
            params: req.params,
        });
        next();
    } catch (error) {
        if (error instanceof ZodError) {
            return next(error); 
        }
        next(error);
    }
};